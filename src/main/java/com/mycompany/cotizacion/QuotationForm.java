    private void operacionDeDatos(){
        int cantDias = Integer.parseInt(cantidadDias.getValue().toString());
        double precioDestino = obtenerPrecioDestino();
        int precioHabitacion = obtenerPrecioHabitacion();
        double subtotal = (cantDias*precioHabitacion)+precioDestino;
        quotation.setSubTotal(subtotal);
        quotation.calculedIva();
        quotation.setTotalPago();
        jtSubtotal.setText(String.valueOf(subtotal));
        jtIva.setText(String.valueOf(quotation.getIva()));
        if(metodoTajeta.isSelected())
        {
            jtTotal.setText(String.valueOf(quotation.getTotal()));
        }else{
            DecimalFormat df = new DecimalFormat("#.00");
            double precio =  quotation.getTotal()/6;
            jtTotal.setText(df.format(precio));
        }
        
    }
    private double obtenerPrecioDestino(){
       double precio=0;
       switch(destinoTuristico.getSelectedIndex())
       {
           case 0:
               precio = 1550;
               break;
            case 1:
               precio = 1650;
               break;
            case 2:
               precio = 1800;
               break;
            case 3:
               precio = 3000;
               break;
            case 4:
               precio = 2000;
               break;
           default:
               precio = 0;
       }
       
       return precio;
    }
    private int obtenerPrecioHabitacion(){
       int precio=0;
       switch(tipoHabitacion.getSelectedIndex())
       {
           case 0:
               precio = 1200;
               break;
            case 1:
               precio = 2100;
               break;
            case 2:
               precio = 3500;
               break;
           default:
               precio = 0;
       }
       
       return precio;
    }
    
