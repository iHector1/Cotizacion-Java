    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       Quotation cotizaciones = new Quotation();
       cotizaciones.setCantDays(Integer.parseInt(cantidadDias.getValue().toString()));
       cotizaciones.setDestination(destinoTuristico.getSelectedItem().toString());
       cotizaciones.setTypeRoom(tipoHabitacion.getSelectedItem().toString());
       int cantDias = Integer.parseInt(cantidadDias.getValue().toString());
       double precioDestino = obtenerPrecioDestino();
       int precioHabitacion = obtenerPrecioHabitacion();
       double subtotal = (cantDias*precioHabitacion)+precioDestino;
       cotizaciones.setSubTotal(subtotal);
       cotizaciones.calculedIva();
       cotizaciones.setTotalPago();
       if(metodoTajeta.isSelected())
        {
            cotizaciones.setPaymentType("A un solo pago");
            cotizaciones.setMonthlyPayment(0);
        }else{
            DecimalFormat df = new DecimalFormat("#.00");
            double precio =  quotation.getTotal()/6;
            cotizaciones.setPaymentType("A 6 meses");
            cotizaciones.setMonthlyPayment(precio);
        }
       QuotationNode node=new QuotationNode(cotizaciones);
       list.addList(node); 
       showMessageDialog(null, "Informacion Guardada");
    }//GEN-LAST:event_btnGuardarActionPerformed

